export const setChat = (chat) => {
    return {
        type: "SET_CHAT",
        payload: chat
    }
};

export const setTitle = (title) => {
    return {
        type: "SET_TITLE",
        payload: title
    }
};

export const setTrip = (trip) => {
    return {
        type: "SET_TRIP",
        payload: trip
    }
};

